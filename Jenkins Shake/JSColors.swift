import UIKit
import Foundation

extension UIColor {
    static var JSBackgroundColor: UIColor {
        get {
            return UIColor(red: 17/255.0, green: 25/255.0, blue: 36/255.0, alpha: 1)
        }
    }
    static var JSOptionsColor: UIColor {
        get {
            return UIColor(red: 34/255.0, green: 49/255.0, blue: 71/255.0, alpha: 1)
        }
    }
}